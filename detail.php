    <h1>Study Techniques</h1>
    <section>
        <h2>Pareto Analysis</h2>

        <p>This technique is also known as the 80/20 rule and based off the idea that "20% of your actions are responsible for 80% of outcomes"</p>

        <ul>
            <li>Identify and list concepts/problems</li>
            <li>Identify the main idea of each concept/problem</li>
            <li>Score each one</li>
            <li>Put them in groups that make sense to you</li>
            <li>Add up scores for each group</li>
            <li>Now start working on the highest scoring group!</li>
        </ul>

        <p>Good for problem solvers and analytical thinkers</p>

    </section>

    <section>
        <h2>Pomodoro Technique</h2>

        <p>This technique is one of the best and most well known studying methods as it makes you feel productive</p>

        <ul>
            <li>Choose a task you need to get done</li>
            <li>Set a timer for 25 minutes and work on the task for that time</li>
            <li>Take a 5 minute break when the time is up</li>
            <li>Repeat this 2-5 times</li>
        </ul>

        <p>Good for creative thinkers and those who are burnt out</p>

    </section>

    <section>
        <h2>Eisenhower Matrix</h2>

        <p>Organize your tasks into the boxes below.</p>

        <img src = "images/eisenhower.png" alt = "eisenhower table">

        <p>Good for people in leadership positions and critical thinkers</p>

    </section>

    <section>
        <h2>Parkinson's Law</h2>

        <p>This technique is more a law than a method.</p>

        <ul>
            <li>Forget the computer charger</li>
            <li>Set an earlier due time</li>
            <li>Set a deadline</li>
            <li>Limit your time for each task</li>
        </ul>

        <p>Good for procrastinators and people who work well under stress</p>

    </section>

    <section>
        <h2>Time blocking method</h2>

        <p>Assign each task to a time block in your schedule. The more detailed the better it could be. For example, eat breakfast, take a shower, study for chem test.</p>

        <ul>
            <li>Use a calendar or piece of paper, we recommend google calendar or notion</li>
            <li>Add the tasks that are already scheduled to the calendar</li>
            <li>Then write down the tasks that aren't scheduled be you need to do</li>
            <li>Then estimate how long it will take for each task</li>
            <li>Add the tasks to your calendar based on time</li>
        </ul>

        <p>Good for students or parents and analytical thinkers</p>

    </section>

    <section>
        <h2>Getting things down method</h2>

        <p>Write down tasks and then break them down into tasks that are more managable and realistic.</p>

        <ul>
            <li>Write down all tasks you want to accomplish</li>
            <li>Determine if you will be able to complete the tasks</li>
            <li>Put your tasks in order of when it needs to be done by</li>
            <li>Look through your list one last time</li>
            <li>Do them!</li>
        </ul>

        <p>Good for those who struggle to focus or those who are overwhelmed in their daily lives</p>

    </section>

    <section>
        <h2>The SQ3R method</h2>

        <p>Helps students identify facts and retain information in their textbook.</p>

        <ul>
            <li>Skim the chapter and take notes on headers, subheaders, images, charts, etc.</li>
            <li>Write down questions about the chapter's content</li>
            <li>Read the chapter to answer your questions you just made</li>
            <li>Then summarize the chapter and identify major points</li>
            <li>Add the tasks to your calendar based on time</li>
        </ul>

        <p>Good for students or parents and analytical thinkers</p>

    </section>

    <section>
        <h2>Retrieval Practice</h2>

        <p>Practice of retrieving learned things.</p>

        <ul>
            <li>Utilize practice tests</li>
            <li>Make your own questions</li>
            <li>Use flashcards</li>
        </ul>

        <p>Good for remembering a lot of information, mainly textbooks and facts</p>

    </section>

